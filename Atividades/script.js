// Adiciona um evento de clique ao botão de mais informações (primeiro)
document.getElementById('infoBtn1').addEventListener('click', function() {
    // Exibe a sobreposição ao clicar no botão
    document.getElementById('overlay1').style.display = 'flex';
});

// Adiciona um evento de clique ao botão de fechar (primeiro)
document.getElementById('closeBtn1').addEventListener('click', function() {
    // Oculta a sobreposição ao clicar no botão de fechar
    document.getElementById('overlay1').style.display = 'none';
});

